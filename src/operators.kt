fun main() {

    //Arithmetic Operators
    var x = 56
    var y = 3

    println(x + y)
    println(x - y)
    println(x / y)
    println(x * y)
    println(x % y)

    println()

    //Comparison Operators
    println(x > y)
    println(x < y)
    println(x >= y)
    println(x <= y)
    println(x == y)
    println(x != y)

    println()

    //Assignment Operators
    var num = 90
    println(num)

    num += 3
    println(num)

    println()

    //Logical Operators - &&(and), ||(or), !(not)
    println(100 < 200 && 50 == 25)
    println(100 < 200 || 50 == 25)
    println(!(100 < 200 || 50 == 25))

}